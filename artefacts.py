class ArtifactBank:  # Создаём класс банка артефактов
    def __init__(self):  # Инициализация объекта
        self.available_artifacts = []  # Список доступных артефактов для игры

    def get_random_artifact(self):  # Метод для случайного выбора артефакта
        if not self.available_artifacts:  # Проверяем, есть ли артефакты
            return None  # Если нет, возвращаем None
        artifact = random.choice(self.available_artifacts)  # Берём случайный артефакт
        self.available_artifacts.remove(artifact)  # Убираем его из банка, чтобы не дать дважды
        return artifact  # Возвращаем выбранный артефакт

    def get_description(self, artifact_name):  # Метод, который возвращает описание артефакта
        return ARTIFACT_DESCRIPTIONS.get(
            artifact_name,  # Ищем описание по имени артефакта
            "Древний артефакт неизвестного происхождения."  # Если не нашли, выводим стандартный текст
        )